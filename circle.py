import math


def area(r):
    ''' 
    Принимает радиус круга r, возвращает площадь круга, вычисленную по формуле Pi * r^2.
    '''
    return math.pi * r * r


def perimeter(r):
    '''Принимает радиус круга r, возвращает периметр круга, вычисленный по формуле 2 * Pi * r'''
    return 2 * math.pi * r

