def area(a, b, c):
    '''Принимает длину сторон треугольника a, b, c, возвращает площадь треугольника по формуле (a + b + c )/ 2'''
    
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Принимает длину сторон треугольника a, b, c, возвращает периметр треугольника по формуле a + b + c '''
    return a + b + c
